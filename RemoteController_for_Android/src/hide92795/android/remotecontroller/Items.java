	public static ItemData[] filter(Context context, String filter) {
		ArrayList<ItemData> result_data = new ArrayList<ItemData>();

		for (ItemData itemdata : items) {
			if (itemdata.match(context, filter)) {
				result_data.add(itemdata);
			}
		}
		if (result_data.size() == 0) {
			return new ItemData[0];
		}
		return result_data.toArray(new ItemData[result_data.size()]);
	}

	public static class ItemData {
		public final String item_id;
		public final int localized_name_string_id;
		public final int default_name_string_id;
		public final int icon_id;
		private String default_lower;
		private String localized_lower;

		private ItemData(String item_id, int localized_name_string_id, int default_name_string_id, int icon_id) {
			this.item_id = item_id;
			this.localized_name_string_id = localized_name_string_id;
			this.default_name_string_id = default_name_string_id;
			this.icon_id = icon_id;
		}

		private String getDefaultLowerName(Context context) {
			if (default_lower == null) {
				default_lower = context.getString(default_name_string_id).toLowerCase(Locale.getDefault());
			}
			return default_lower;
		}

		private String getLocalizedLowerName(Context context) {
			if (localized_lower == null) {
				localized_lower = context.getString(localized_name_string_id).toLowerCase(Locale.getDefault());
			}
			return localized_lower;
		}

		public boolean match(Context context, String filter) {
			String lower_filter = filter.toLowerCase(Locale.getDefault());
			if (item_id.startsWith(lower_filter)) {
				return true;
			}
			if (getDefaultLowerName(context).contains(lower_filter)) {
				return true;
			}
			if (getLocalizedLowerName(context).contains(lower_filter)) {
				return true;
			}
			return false;
		}
	}
